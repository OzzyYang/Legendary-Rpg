public class UISkillToolTipController : UIToolTipController
{
	public override void Hide()
	{
		base.Hide();
	}

	public override void Show()
	{
		base.Show();
	}

	public override void Show(string title, string content)
	{
		base.Show(title, content);
	}

	public override void Show(string title, string type, string content)
	{
		base.Show(title, type, content);
	}

	protected override void Update()
	{
		base.Update();
	}

}
